package javaapplication1;

public class JavaApplication1 {

    public static void main(String[] args) {
        
       // Switch case
       int a = 8; 
       
       switch(a){
           
            case 0:
               System.out.println("Zero");
               break;
            case 1:
               System.out.println("One");
               break;
            case 2:
               System.out.println("Two");
               break;
            default:
               System.out.println("Other number");
           
       }

        
    }
    
}
